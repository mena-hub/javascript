function add(a, b) {
    return a + b;
}

function subtract(a, b) {
    return a - b;
}

function multiply(a, b) {
    return a * b;
}

function divide(a, b) {
    return a / b;
}

console.log("Resultado + ", add(2, 2));
console.log("Resultado - ", subtract(2, 2));
console.log("Resultado * ", multiply(2, 2));
console.log("Resultado / ", divide(2, 2));